package Tabel;

public class pegawai 
{
    
    private String id, nama, gender, alamat, email, no_hp;
    
    public String get_id_pegawai()
    {
        return id;
    }
    
    public void set_id_pegawai(String x)
    {
        id = x;
    }
    
    public String get_nama_pegawai()
    {
        return nama;
    }
    
    public void set_nama_pegawai(String x)
    {
        nama = x;
    }
    
    public String get_gender_pegawai()
    {
        return gender;
    }
    
    public void set_gender_pegawai(String x)
    {
        gender = x;
    }
    public String get_alamat_pegawai()
    {
        return alamat;
    }
    
    public void set_alamat_pegawai(String x)
    {
        alamat = x;
    }
    
    public String get_email_pegawai()
    {
        return email;
    }
    
    public void set_email_pegawai(String x)
    {
        email = x;
    }
    
    public String get_no_hp_pegawai()
    {
        return no_hp;
    }
    
    public void set_no_hp_pegawai(String x)
    {
        no_hp = x;
    }
}
