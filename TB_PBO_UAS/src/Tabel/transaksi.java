package Tabel;

public class transaksi 
{
    private String id, tanggal;
    private double total_transaksi, total_bayar;
    private String pegawai;
    
    public String get_id_transaksi()
    {
        return id;
    }
    
    public void set_id_transaksi(String x)
    {
        id = x;
    }
    
    public String get_tanggal_transaksi()
    {
        return tanggal;
    }
    
    public void set_tanggal_transaksi(String x)
    {
        tanggal = x;
    }
    
    public double get_total_transaksi_transaksi()
    {
        return total_transaksi;
    }
    
    public void set_total_transaksi_transaksi(double x)
    {
        total_transaksi = x;
    }
    
    public double get_total_bayar_transaksi()
    {
        return total_bayar;
    }
    
    public void set_total_bayar_transaksi(double x)
    {
        total_bayar = x;
    }
    
    public String get_pegawai_transaksi()
    {
        return pegawai;
    }
    
    public void set_pegawai_transaksi(String x)
    {
        pegawai = x;
    }
}
