    private void cariMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cariMouseClicked
        String a = textCari.getText();
        String query = "select * from barang where id like '%"+a+"%' or nama like '%"+a+"%' or harga_jual like '"+a+"' or kategori like '%"+a+"%'";
        
        barang = new Kontrol.kontrol_barang();
        
        try
        {
           modelPembelian.getDataVector().removeAllElements();
           rs = barang.execQuery(query);
            
            while(rs.next())
            {
                Object[] x = new Object[5];
                x[0] = rs.getString("id");
                x[1] = rs.getString("nama");
                x[2] = rs.getString("harga_jual");
                x[3] = rs.getString("stok");
                x[4] = rs.getString("kategori");
                modelPembelian.addRow(x);
            }
            rs.close();
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, "Barang gagal ditampilkan error : "+e);
        }
    }//GEN-LAST:event_cariMouseClicked

