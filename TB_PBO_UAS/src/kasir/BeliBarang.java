    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here:
        DefaultTableModel zz = (DefaultTableModel)pembelian.getModel();
        int baris = pembelian.getSelectedRow();
        
        String cek = JOptionPane.showInputDialog("Masukkan jumlah barang");
        String stok = (String)zz.getValueAt(baris, 3);
        int j  = Integer.parseInt(stok);
        
        if(j <= Integer.valueOf(cek))
        {
            JOptionPane.showMessageDialog(this, "Stok tidak mencukupi atau habis!");
        }
        else
        {
            String[] x = new String[5]; 
            for (int i=0; i<3; i++)
            {
                x[i] = zz.getValueAt(baris, i).toString();
            }
            x[3] = cek;
            x[4] = String.valueOf(Double.valueOf(x[3]) * Double.valueOf(x[2]));
            modelKeranjang.addRow(x);
        }
        
    }//GEN-LAST:event_tambahActionPerformed

