    private void panggil()
    {
        listPegawai.getDataVector().removeAllElements();
        pegawai = new Kontrol.kontrol_pegawai();
        String query = "select * from pegawai";
        try
        {
            ResultSet rs = pegawai.execQuery(query);
            
            while(rs.next())
            {
                Object[] x = new Object[6];
                
                x[0] = rs.getString("id");
                x[1] = rs.getString("nama");
                x[2] = rs.getString("gender");
                x[3] = rs.getString("alamat");
                x[4] = rs.getString("email");
                x[5] = rs.getString("no_hp");
                listPegawai.addRow(x);
            }
        }
        catch(SQLException ex)
        {
            System.out.println("Isi tabel gagal dipanggil, error : "+ex);
        }
    }
