    private void hapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_hapusMouseClicked
        // TODO add your handling code here:
        int row = tabelPegawai.getSelectedRow();
        
        Runnable r = () -> {
            String html = "<html><body width='150'>"
                +"<p>meskipun akan ada pesan data telah terhapus"
                + " Isi tabel tidak akan terhapus"
                + "jika akun dari pegawai belum dihapus</p><br>"
                +"<br><br><p>pastikan akun telah terhapus terlebih dahulu</p>";
            // change to alter the width 
            int w = 150;

            JOptionPane.showMessageDialog(null, String.format(html, w, w));
        };
        SwingUtilities.invokeLater(r);
        int confirm = JOptionPane.showConfirmDialog(null, "Kamu yakin menghapus "+String.valueOf(tabelPegawai.getValueAt(row, 0))+"? ", "WARNING", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION)
        {     
            Tabel.pegawai x = new Tabel.pegawai();
            
            x.set_id_pegawai(String.valueOf(tabelPegawai.getValueAt(row, 0)));
            pegawai.delete_pegawai(x);
            JOptionPane.showMessageDialog(this, "data telah terhapus!");
        }
    }//GEN-LAST:event_hapusMouseClicked

    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        // TODO add your handling code here:
        int row = tabelPegawai.getSelectedRow();
        int confirm = JOptionPane.showConfirmDialog(null, "Kamu yakin mengubah "+String.valueOf(tabelPegawai.getValueAt(row, 0))+"? ", "WARNING", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION)
        {     
            Tabel.pegawai x = new Tabel.pegawai();
            x.set_id_pegawai(String.valueOf(tabelPegawai.getValueAt(row, 0)));
            x.set_nama_pegawai(String.valueOf(tabelPegawai.getValueAt(row, 1)));
            x.set_gender_pegawai(String.valueOf(tabelPegawai.getValueAt(row, 2)));
            x.set_alamat_pegawai(String.valueOf(tabelPegawai.getValueAt(row, 3)));
            x.set_email_pegawai(String.valueOf(tabelPegawai.getValueAt(row, 4)));
            x.set_no_hp_pegawai(String.valueOf(tabelPegawai.getValueAt(row, 5)));
            pegawai.update_pegawai(x);
            JOptionPane.showMessageDialog(this, "data telah terupdate");
            panggil();
        }
    }//GEN-LAST:event_updateMouseClicked

